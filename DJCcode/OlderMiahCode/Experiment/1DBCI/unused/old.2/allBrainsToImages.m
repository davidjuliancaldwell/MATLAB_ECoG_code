targets = {'jc', ...
        'hh', ...
        '4568f4', ...
        '26cb98', ...
        '30052b', ...
        'mg', ...
        'jt2', ...
        '04b3d5', ...
        'fc9643', ...
        '38e116'};
    
outDir = [myGetenv('output_dir') '\remoteAreas'];

for target = targets
    tgt = target{:};
    e_tgt = ['e_' tgt];
    
    if (strcmp(tgt, '30052b') == 1 || ...
        strcmp(tgt, 'fc9643') == 1 )
        PlotCortex(e_tgt, 'r');
    else
        PlotCortex(e_tgt);
    end
    
    PlotElectrodes(e_tgt);
    maximize;

    if (strcmp(tgt, 'jc') == 1 || ...
        strcmp(tgt, 'mg') == 1 || ...
        strcmp(tgt, '04b3d5') == 1 )
        view(270,0);
    else
        view(90,0);
    end
    
    SaveFig(outDir, ['electrodes_' tgt]);
    close all;
end