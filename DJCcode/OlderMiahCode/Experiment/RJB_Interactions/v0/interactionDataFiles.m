function [files, hemi, Montage, controlChannel] = interactionDataFiles(subjid)
    if(strcmp(subjid,'30052b'))
        [files, hemi, Montage, controlChannel] = do30052B();
    elseif(strcmp(subjid,'fc9643'))
        [files, hemi, Montage, controlChannel] = doFC9643();
    else
        error('subjid not found');
    end
end

